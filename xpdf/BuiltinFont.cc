const builtin_font_t *builtin_font(const char *s)
{
    for (size_t i = 0; i < detail::sizeof_(detail::builtin_fonts); ++i) {
        if (0 == detail::builtin_fonts[i].name.compare(s))
            return detail::builtin_fonts + i;
    }

    return 0;
}

